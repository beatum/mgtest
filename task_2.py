# ------------------------------------------------------------------------------
# Вывод всех чисел оператором while от 45 до 670, которые кратны 10
# ------------------------------------------------------------------------------

while 1:
    for i in range(45, 670):
        if i % 10 == 0:
            print(i)
    break

